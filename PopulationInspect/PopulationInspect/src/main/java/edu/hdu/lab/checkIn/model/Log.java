package edu.hdu.lab.checkIn.model;

public class Log {
	private int id;							
	private int taskId;
	private String content;					//日志正文
	private String date;					//提交日期
	public int getId() {
		return id;
	}
	
	public int getTaskId() {
		return taskId;
	}

	public void setTaskId(int taskId) {
		this.taskId = taskId;
	}

	public void setId(int id) {
		this.id = id;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public Log() {

	}
	
	public Log(int id, int taskId, String content, String date) {
		super();
		this.id = id;
		this.taskId = taskId;
		this.content = content;
		this.date = date;
	}

	@Override
	public String toString() {
		return "Log [id=" + id + ", taskId=" + taskId + ", content=" + content
				+ ", date=" + date + "]";
	}


	
}
